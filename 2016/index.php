<script>
    var loaded = false;
    (function ($) {
        $.fn.goTo = function () {
            $('html, body').animate({
                scrollTop: $(this).offset().top + 'px'
            }, 2000);
            return this; // for chaining...
        }
    })(jQuery);

    $(document).ready(function () {
        setTimeout(function () {
            if (!loaded) {
                $(".loadingSlide").find(".lead").append("<br><a href='#' class='closeLoading'>Loading too slow? View Story Anyways</a>");
            }
        }, 10 * 1000);
    });

    $(".scroll").on('click', function (e) {
        e.preventDefault();
        console.log("a");
        var parent = $(this).parent();
        parent.fadeOut('slow');
        setTimeout(function () {
            parent.show();
        }, 2000);
        var go = $(this).data("scroll");
        $(go).find('.lead').hide();
        setTimeout(function () {
            $(go).find('.lead').fadeIn(1500);
        }, 2000);
        $(go).goTo();
    });

    $(document).on('click', ".closeLoading", function (e) {
        $(".loadingSlide").fadeOut("slow");
    });

    $(window).load(function () {
        loaded = true;
        setTimeout(function () {
            $(".loadingSlide").fadeOut("slow");
        }, 700);
    });
</script>
